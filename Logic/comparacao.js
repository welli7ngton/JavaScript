/*
Operadores de comparação
>    maior que
>=   maior que ou igual
<    menor que
<=   menor que ou igual
==   igualdade (valor) ***não usar
===  igualdade estrita (valor e tipo)
!=   diferente (valor) ***não usar
!==  diferente estrito (valor e tipo)
*/


// maior/menor ou igual
console.log(2 > 1);
console.log(2 >= 2);
console.log(2 > 2);
console.log(1 <= 2);
console.log();

console.log(10 == '10');
console.log(10 == 10);
console.log(10 === '10');
console.log();

console.log(11 != 10);
console.log(11 != "11");
console.log(11 !== "11");